var content_items = " ";
var prices =0;
document.getElementById("product1_btn").onclick=function()
{
  var product_price = document.getElementById("price1").textContent;
  var content=document.getElementById("bookname1").textContent;
  content_items+=content+", ";
  prices+=parseInt(product_price);
  document.getElementById("total_price").innerHTML=" "+prices+" EGP";
  document.getElementById("items").innerHTML=content_items;
}
